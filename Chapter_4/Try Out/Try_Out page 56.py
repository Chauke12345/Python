# List of favorite pizzas
pizzas = ["Pepperoni", "Margherita", "BBQ Chicken"]

# Loop through the list and print a sentence about each pizza
for pizza in pizzas:
    print("I like " + pizza + " pizza.")

# Statement outside the loop
print("I really love pizza!")

# List of animals with a common characteristic (they can be good pets)
animals = ["Dog", "Cat", "Rabbit"]

# Loop through the list and print a statement about each animal
for animal in animals:
    print("A " + animal + " would make a great pet.")

# Statement outside the loop about what they have in common
print("Any of these animals would make a great pet!")